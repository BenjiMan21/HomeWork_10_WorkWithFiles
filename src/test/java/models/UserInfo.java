package models;

public class UserInfo {
    private String name;
    private int age;
    private String[] teachersName;
    private CourseInfo courseInfo;

    public String getName() {
        return name;
    }

    public int getAge() {

        return age;
    }

    public String[] getTeachersName() {

        return teachersName;
    }

    public CourseInfo getCourseInfo() {

        return courseInfo;
    }

}


