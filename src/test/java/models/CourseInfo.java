package models;

public class CourseInfo {

    private String language;
    private int group;

    public String getLanguage() {
        return language;
    }
    public void setLanguage(String language) {
        this.language = language;
    }


    public int getGroup() {
        return group;
    }
    public void setGroup(int group) {
        this.group = group;
    }

}
